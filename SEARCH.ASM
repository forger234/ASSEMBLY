ASSUME CS:CODE, DS:DATA
DATA SEGMENT
    NUMLIST DB 11H,22H,33H,44H,55H
    MSG1 DB 0AH,0DH, "NUMBER IS FOUND$", 0
    MSG2 DB 0AH,0DH, "NUMBER IS NOT FOUND$", 0
    SE DB 39H
DATA ENDS
PRINT MACRO MSG
    MOV AH,09H
    LEA DX,MSG
    INT 21H
    ENDM
CODE SEGMENT
START:
MOV AX, DATA
MOV DS, AX
MOV CX, 05H
LEA SI, NUMLIST
MOV AL, SE
UP:
    MOV BL, [SI]
    CMP AL, BL
    JZ FO
    INC SI
    DEC CX
    JNZ UP
PRINT MSG2
JMP END1
FO:
PRINT MSG1
END1:
MOV AH, 4CH
INT 21H
CODE ENDS
END START